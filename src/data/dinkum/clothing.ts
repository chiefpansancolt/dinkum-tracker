export const getClothingById = (id: string): Clothing | undefined => {
  return clothing.find((item) => item.id === id);
};

export const getClothingByType = (
  data: Clothing[],
  type: string,
): Clothing[] => {
  return data.filter((item) => item.type === type);
};

export const getClothingBySlot = (
  data: Clothing[],
  slot: ClothingSlot,
): Clothing[] => {
  return data.filter((item) => item.slot.includes(slot));
};

export const getClothingBySet = (data: Clothing[], set: string): Clothing[] => {
  return data.filter((item) => item.set === set);
};

export const getClothingBySearchValue = (
  data: Clothing[],
  searchValue: string,
): Clothing[] => {
  return data.filter((item) =>
    item.name.toLowerCase().includes(searchValue.toLowerCase()),
  );
};

export const getUniqueClothingSlots = (): FilterArray => {
  return ["All", ...Object.keys(ClothingSlots)];
};

export const getUniqueClothingSets = (): FilterArray => {
  const sets = new Set<string>();
  clothing.forEach((item) => {
    if (item.set) {
      sets.add(item.set);
    }
  });

  return ["All", ...Array.from(sets).sort()];
};
