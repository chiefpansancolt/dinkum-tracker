export const getFurnitureBySource = (
  data: Furniture[],
  value: string,
): Furniture[] => {
  return data.filter((item) => item.source?.includes(value));
};

export const getFurnitureBySet = (
  data: Furniture[],
  value: string,
): Furniture[] => {
  return data.filter((item) => item.furnitureSet === value);
};

export const getFurnitureBySearchValue = (
  data: Furniture[],
  searchValue: string,
): Furniture[] => {
  return data.filter((item) =>
    item.name.toLowerCase().includes(searchValue.toLowerCase()),
  );
};

export const getUniqueFurnitureSources = (): FilterArray => {
  const sources = new Set<string>();

  furniture.forEach((item) => {
    if (item.source) {
      item.source.forEach((s) => {
        sources.add(s);
      });
    }
  });

  return ["All", ...Array.from(sources).sort()];
};

export const getUniqueFurnitureSets = (): FilterArray => {
  const sets = new Set<string>();
  furniture.forEach((item) => {
    if (item.furnitureSet) {
      sets.add(item.furnitureSet);
    }
  });

  return ["All", ...Array.from(sets).sort()];
};
